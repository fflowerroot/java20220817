package ch03.book;

public class CompareOperatorExample1 {
	public static void main(String[] args) {
		int i1 = 10;
		int i2 = 10;
		boolean r1 = i1 == i2;
		boolean r2 = i1 != i2;
		boolean r3 = i1 <= i2;
		System.out.println(r1);
		System.out.println(r2);
		System.out.println(r3);
		
		char c1 = 'a';
		char c2 = 'b';
		boolean r4 = c1 < c2;
		System.out.println(r4);
		
	}

}
