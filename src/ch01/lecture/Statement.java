package ch01.lecture;

public class Statement {
	public static void main(String[] args) {
		/* 명령문 	: 컴퓨터에게 일을 시키는 의미있는 코드
		 * 프로그램 : 명령문의 나열
		 * 명령문은 순서대로 실행됨
		 * 대부분의 명령문은 ';'으로 끝남
		 * 명령문은 main method 내에 작성
		 * main method : 프로그램이 가장 먼저 시작되는 곳. main method가 있어야 실행된다.
		 */
		System.out.println("..");
	}
}
