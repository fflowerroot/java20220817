package ch04.lecture.p01control;

public class C05Switch {
	public static void main(String[] args) {
		// if로 해결가능하므로 switch문이 없는 언어도 있다
		switch (100) {
		case 100:
			System.out.println(2);
			System.out.println(3);
			break;
		case 200:
			System.out.println(4);
			System.out.println(5);
		}
	}
}